                    UnitOfExamples.Examples.DataType_Class.Boolean_Extended_Syntax.oneLiner_1_Step();
                    UnitOfExamples.Examples.DataType_Class.Boolean_Extended_Syntax.setThenConvert_2_Steps(); 
